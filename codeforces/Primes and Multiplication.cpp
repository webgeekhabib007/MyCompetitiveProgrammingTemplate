void solve(ll cases=0){
    ll n;cin>>n;
    vector<ll> a(n,0);
    for(auto &x: a)cin>>x;
    ll ans=0;
    sort(all(a));
    map<ll,ll> m;
    for(auto it:a){
        m[it]++;
    }
    vector<ll> v;
    for(auto it:m){
        v.push_back(it.first);
    }
  
 
    for(ll i=v.size()-1;i>=0;i--){
        ll cur=v[i];
        ll len=m[cur];
        ll f=0;
        for(int j=i-1;j>=0;j--){
            int lc= cur*v[j];
            lc=lc/__gcd(cur,v[j]);
            
            if(lc>v.back()){
                f=1;
                len=n;break;
            }
            if(m[lc]>0 and lc!=cur){continue;}
            len=len+m[v[j]];
            cur=lc;
            if(m.find(cur)==m.end()){
                ans=max(ans,len);
            }
            
            
        }
        if(f){
            ans=n;break;
        }
        // cout<<i<<" "<<len<<endl;
        if(len==1){continue;}
        if(m[cur]>0){continue;}
        ans=max(ans,len);
        if(ans==n){break;}
    }
    cout<<ans<<endl;
}


