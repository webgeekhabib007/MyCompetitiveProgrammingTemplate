string problem_name = "\"Primes and Multiplication\"";

const ull mod = 1e9+7;

vector<ll> binDec;
bool is(ll n){
    while(n){
        ll rem = n%10;
        if(rem>1)return false;
        n/=10;
    }
    return true;
}
auto preCompute = [](){
    for(ll i=2;i<=100005;i++){
        if(is(i)){
            binDec.push_back(i);
        }
    }    
};
vector<bool> dp(100005,false);

bool ok(ll n){
    if(n==1)return true;
    if(dp[n])return true;
    bool ans = false;
    for(auto i : binDec){
        if(n%i==0){
            ans|= ok(n/i);
        }
    }
    return dp[n]=ans;
}



void solve(ll cases=0){
    ll n;cin>>n;
    cout << (ok(n)? "YES" : "NO") << endl;
}


