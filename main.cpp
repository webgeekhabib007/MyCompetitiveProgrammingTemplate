string problem_name = "\"Primes and Multiplication\"";

const ull mod = 1e9+7;



void solve(ll cases=0){
    ull x,n;
    cin>>x>>n;
    vector<ull> v;
    for(ull i=2;i*i<=x;i++){
        if(x%i==0){
            v.push_back(i);
            while(x%i==0)x/=i;
        }
    }
    if(x>1){
        v.push_back(x);
    }
    
    function<ull(ull,ull,ull)> binmod = [&](ull n,ull k,ull m)->ull{
        if(k==0ULL)return 1ULL;
        if(k==1ULL)return n;
        if(k%2==0){
            ull tmp = binmod(n,k/2,mod);
            return (tmp%mod*tmp%mod)%mod;
        }
        return (n%mod*binmod(n,k-1,mod)%mod)%mod;
    };

    ull ans = 1ULL;
    for(auto primes : v){
        ull fact = n;
        ull cnt = 0;
        while(fact){
            fact/=primes;
            cnt+= fact;
        }
        ans*= binmod(primes,cnt,mod);
        ans%=mod;
    }
    debug(v);
    cout << ans << nl;
}


