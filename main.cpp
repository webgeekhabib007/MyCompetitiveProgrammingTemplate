void solve(ll cases=0){
    string s;
    cin>>s;
    deque<char> q;
    stack<char> st;
    ll cnt=0;
    for(auto x: s){
        q.push_back(x);
    }
    while(!q.empty()){
        debug(q);
        if(st.empty()){
            st.push(q.front());
            q.pop_front();
        }else{
            if(st.top()=='A' and q.front()=='B'){
                st.pop();
                q.pop_front();
                q.push_front('C');
                q.push_front('B');
                cnt++;
            }else if(st.top()=='B' and q.front()=='A'){
                st.pop();
                q.pop_front();
                q.push_front('B');
                q.push_front('C');
                cnt++;
            }else{
                st.push(q.front());
                q.pop_front();
            }
        }
    }
    cout << cnt << nl;
}


